package String;

public class Countnoofstring {

	public static void main(String[] args) {
		String string="the best of both words";
		int count=0;
		for(int i=0;i<string.length();i++)
		{
			if(string.charAt(i)!=' ');
			count++;
			
		}
		System.out.println("total no of counts:"+count);

	}

}
